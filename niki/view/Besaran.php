			<p>
				Satuan adalah sebagai pembanding dalam suatu pengukuran besaran. Setiap besaran mempunyai satuan masing-masing, tidak mungkin dalam 2 besaran yang berbeda mempunyai satuan yang sama. Apa bila ada dua besaran berbeda kemudian mempunyai satuan sama maka besaran itu pada hakekatnya adalah sama. Sebagai contoh Gaya (F) mempunyai satuan Newton dan Berat (w) mempunyai satuan Newton. Besaran ini kelihatannya berbeda tetapi sesungguhnya besaran ini sama yaitu besaran turunan gaya.<br><br>

				a. Satuan Baku<br><br>

				Satuan baku adalah satuan yang telah diakui dan disepakati pemakaiannya secara internasional tau disebut dengan satuan internasional (SI).<br><br>
				Contoh: meter, kilogram, dan detik.<br>
				Sistem satuan internasional dibagi menjadi dua, yaitu:<br>
				1. Sistem MKS (Meter Kilogram Sekon)<br>
				2. Sistem CGS (Centimeter Gram Second)<br>
				b. Satuan Tidak Baku<br>

				Satuan tidak baku adalah satuan yang tidak diakui secara internasional dan hanya digunakan pada suatu wilayah tertentu.
				Contoh: depa, hasta, kaki, lengan, tumbak, bata dan langkah.<br><br>

				ALAT UKUR<br><br>
				Alat Ukur adalah sesuatu yang digunakan untuk mengukur suatu besaran.
				Berbagai macam alat ukur memiliki tingkat ketelitian tertentu. Hal ini bergantung pada skala terkecil alat ukur tersebut. Semakin kecil skala yang tertera pada alat ukur maka semakin tinggi ketelitian alat ukur tersebut. Beberapa contoh alat ukur sesuai dengan besarannya, yaitu:<br><br>
				a. Alat Ukur Panjang<br><br>

				1. Mistar (Penggaris)<br><br>

				Mistar adalah ala ukur panjang dengan ketelitian sampai 0,1 cm atau 1 mm. Pada pembacaan skala, kedudukan mata pengamat harus tegak lurus dengan skala mistar yang di baca.<br><br>

				2. Jangka Sorong<br><br>
				Jangka sorong dipakai untuk mengukur suatu benda dengan panjang yang kurang dari 1mm. Skala terkecil atau tingkat ketelitian pengukurannya sampai dengan 0,01 cm atau 0,1 mm.
				Umumnya, jangka sorong digunakan untuk mengukur panjang suatu benda, diameter bola, ebal uang logam, dan diameter bagian dalam tabung.
				Jangka sorong memiliki dua skala pembacaan, yaitu:<br><br>

				a). Skala Utama/tetap, yang terdapat pada rahang tetap jangka sorong.<br>

				b). Skala Nonius, yaitu skala yang terdapat pada rahang sorong yang dapa bergeser/digerakan.<br><br>

				3. Mikrometer Sekrup<br><br>

				Mikrometer sekrup merupakan alat ukur panjang dengan ingkat ketelitian terkecil yaiu 0,01 mm atau 0,001 cm.
				Skala terkecil (skala nonius) pada mikrometer sekrup terdapat pada rahang geser, sedangkan skala utama terdapat pada rahang tetap.
				Mikrometer sekrup digunakan untuk mengukur diameter benda bundar dan plat yang sangat tipis.<br><br>

				b. Alat Ukur Massa<br><br>

				Alat ukur yang digunakan untuk mengukur massa suatu benda adalah neraca. Berdasarkan cara kerjanya dan keelitiannya neraca dibedakan menjadi tiga, yaitu:<br><br>

				1. Neraca digital, yaitu neraca yang bekerja dengan sistem elektronik. Tingkat ketelitiannya hingga 0,001g.<br>
				2. Neraca O’Hauss, yaitu neraca dengan tingkat ketelitian hingga 0.01g.<br>
				3. Neraca sama lengan, yaitu neraca dengan tingkat ketelitian mencapai 1 mg atau 0,001 g.<br><br>
				c. Alat Ukur Waktu<br><br>

				Satuan internasional untuk waktu adalah detik atau sekon. Satu sekon standar adalah waktu yang dibuuhkan oleh atom Cesium-133 untuk bergetar sebanyak 9.192.631.770 kali.<br><br>

				Alat yang digunakan untuk mengukur waktu, antara lain jam matahari, jam dinding, arloji (dengan ketelitian 1 sekon), dan stopwatch (ketelitian 0,1 sekon).<br><br>

				Besaran berdasarkan arah dapat dibedakan menjadi 2 macam<br>

				Besaran vektor adalah besaran yang mempunyai nilai dan arah sebagai contoh besaran kecepatan, percepatan dan lain-lain.
				Besaran sekalar adalah besaranyang mempunyai nilai saja sebagai contoh kelajuan, perlajuan dan lain-lain.<br><br>
