			<p>
				Pengukuran adalah proses membandingkan nilai besaran yang diukur dengan besaran sejenis yang dipakai sebagai satuan. Hasil dari pada pengukuran merupakan besaran. Besaran adalah suatu yang dapat diukur dan dinyatakan dengan angka dan nilai yang memiliki satuan.<br><br>

				Dari pengertian ini dapat diartikan bahwa sesuatu itu dapat dikatakan sebagai besaran harus mempunyai 3 syarat yaitu<br><br>

				1. dapat diukur atau dihitung<br>
				2. dapat dinyatakan dengan angka-angka atau mempunyai nilai<br>
				3. mempunyai satuan<br><br>

				Bila ada satu saja dari syarat tersebut diatas tidak dipenuhi maka sesuatu itu tidak dapat dikatakan sebagai besaran.<br><br>

				Besaran berdasarkan cara memperolehnya dapat dikelompokkan menjadi 2 macam yaitu :<br><br>

				1. Besaran Fisika yaitu besaran yang diperoleh dari pengukuran. Karena diperoleh dari pengukuran maka harus ada alat ukurnya. Sebagai contoh adalah massa. Massa merupakan besaran fisika karena massa dapat diukur dengan menggunakan neraca.<br><br>
				2. Besaran non Fisika yaitu besaran yang diperoleh dari penghitungan. Dalam hal ini tidak diperlukan alat ukur tetapi alat hitung sebagai misal kalkulator. Contoh besaran non fisika adalah Jumlah.<br><br>

				Dalam fisika besaran ada dua yaitu besaran pokok dan besaran turunan.<br><br>

				Besaran pokok adalah besaran yang satuannya telah ditetapkan terlebih dahulu dan tidak diturunkan dari besaran lain.<br><br>

				Besaran pokok yang paling umum ada 7 macam yaitu Panjang (m), Massa (kg), Waktu (s), Suhu (K), Kuat Arus Listrik (A), Intensitas Cahaya (cd), dan Jumlah Zat (mol). Besaran pokok mempunyai ciri khusus antara lain diperoleh dari pengukuran langsung, mempunyai satu satuan (tidak satuan ganda), dan ditetapkan terlebih dahulu.<br><br>



